

# 切片高级

a = [1,2,3,4,5,6,7,8,9.0]
print(a[0:3])

i= 0
while i<3:
    print(a[i])
    i+=1

print(a[0:5:2])

print(a[-5:-1])

print(a[2:])
print(a[::2])

